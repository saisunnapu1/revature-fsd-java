package Polymorphism;

public class Overloading {
	void add(int x, int y) {
	   int	k=x+y;
	   System.out.println(k);
		
	}
      void add(int x,int y,int z) {
    	  float l=x+y+z;
    	  System.out.println(l);
      }
	public static void main(String[] args) {
		Overloading o=new Overloading();
		o.add(5,9);
		

	}

}
